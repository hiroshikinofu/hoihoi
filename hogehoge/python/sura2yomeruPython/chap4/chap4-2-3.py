def create_mail(recv):
     # create_mailという名前で、引数recvを受けとる以下の内容の関数を作る
    print(recv, '様')
     # 引数recvと文字列「様」を表示しろ
    print('PT企画の斉藤です。')
     # 文字列「PT企画の斉藤です。」を表示しろ
    print('今月の請求書を送ります。')
     # 文字列「今月の請求書を送ります。」を表示しろ

create_mail('山本')
     # 文字列「山本」を指定してメールを作れ
create_mail('吉田')
     # 文字列「吉田」を指定してメールを作れ
    
